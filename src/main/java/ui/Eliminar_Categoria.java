    private void JBotonEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBotonEliminar1ActionPerformed
        String nombre = (String) JComboBoxCategorias.getSelectedItem();
        
        if (nombre.equalsIgnoreCase("Sin categoría")) {
            JOptionPane.showMessageDialog(this,
                "La categoría 'Sin categoría' no puede eliminarse.",
                "Acción no permitida",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (nombre == null || nombre.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar una categoría.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        int opcion = JOptionPane.showConfirmDialog(
                this,
                "¿Seguro que desea eliminar la categoría '" + nombre + "'?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (opcion != JOptionPane.YES_OPTION) return;

        CategoriaDao dao = new CategoriaDao();
        boolean eliminada = dao.eliminarCategoria(nombre);

        if (eliminada) {
            JOptionPane.showMessageDialog(this,
                    "Categoría eliminada correctamente.",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);

            // Recargar combo de categorías
            recargarCategorias();
            JTextDescripcion.setText("");

        } else {
            JOptionPane.showMessageDialog(this,
                    "No se pudo eliminar la categoría.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JBotonEliminar1ActionPerformed

    private void JComboBoxCategoriasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JComboBoxCategoriasActionPerformed
        String nombre = (String) JComboBoxCategorias.getSelectedItem();
        if (nombre == null || nombre.trim().isEmpty()) {
            JTextDescripcion.setText("");
            return;
        }

        CategoriaDao dao = new CategoriaDao();
        String descripcion = dao.descripcionPorNombre(nombre);

        if (descripcion != null) {
            JTextDescripcion.setText(descripcion);
        } else {
            JTextDescripcion.setText("");
        }
    }//GEN-LAST:event_JComboBoxCategoriasActionPerformed

