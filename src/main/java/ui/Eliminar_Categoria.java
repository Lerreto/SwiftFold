    private void JBotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBotonGuardarActionPerformed
        String nombre = (String) JComboBoxCategorias.getSelectedItem();
        String nuevaDescripcion = JTextDescripcion.getText().trim();

        if (nombre == null || nombre.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar una categoría.",
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        CategoriaDao dao = new CategoriaDao();
        boolean ok = dao.modificarCategoria(nombre, nuevaDescripcion);

        if (ok) {
            JOptionPane.showMessageDialog(this,
                    "Descripción actualizada correctamente.",
                    "Éxito",
                    JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(this,
                    "No se pudo actualizar la categoría.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JBotonGuardarActionPerformed

